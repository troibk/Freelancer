package htmlparser;

public class Email {
	String website;
	String email;
	public Email(String website, String email) {
		super();
		this.website = website;
		this.email = email;
	}
	public String getWebsite() {
		return website;
	}
	public void setWebsite(String website) {
		this.website = website;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
}
